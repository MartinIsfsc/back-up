  <h1 id="presentation_titre">Présentation de l'ASBL</h1>



  <div class="presentation_chapeau">
    <p id="chapeau">
     The Layers Project est une association sans but lucratif ayant le but
     de promouvoir une manière de penser l'urbanisme que nous considérons très efficace 
     auprès des citoyens et des institutions publiques. C'est un incubateur d'idées et
     de solutions pour faire face aux défis de la mobilité du 21ieme siècle, 
     notamment car de plus en plus d'humains vivent en ville. Ceci est donc 
     un site vitrine de notre vision d’une ville parfaitement adaptée à la mobilité. 
     Son but est d'expliquer et d'informer le grand public mais aussi les professionnels et 
     politiques sur ce concept. 
    </p>
  </div>
  
  

  <div class="presentation_text">
    <p id="asbl_text"> 
 
    <br>
    <br>
    Les cités contemporaines se doivent donc d'être adaptées à un nombre croissant d'habitants
    tout en proposant une mobilité restant agréable et polyvalente. Nous défendons donc une
    nouvelle solution à la mobilité contemporaine en
    proposant le concept de séparation des différents types de transports. De ce fait nous mettons
    en avant la modernité, l'écologie et le confort du transport.
    <br>
    <br>
    Notre principe le plus important pour une mobilité efficace réside donc en la séparation
    des flux des différents moyens de transport, car si différents moyens de transport
    croisent leur chemins, afin de rester sécurisé la voirie se doit d'accorder le temps
    nécessaire pour chaque type de véhicule différent.
    <br>
    <br>
    Le problème étant que ce moment accordé s'ajoute aux temps de trajet des autres types de
    véhicules impliqués dans le croisement ce qui au bout du compte ajoute un retard
    considérable pour chaque trajet.
    <br>
    <br>
    Notre solution : faire en sorte qu'aucun moyen de transport différent n'ait à se croiser,
    ce qui amène à une optimisation globale de la circulation des véhicules.
    De plus nous cherchons à rendre les différents transports en commun plus optimisés en
    pensant leur utilisation adaptée à la manière dont ils sont le plus efficace.
    <br>
    <br>
    Par exemple Le bus est très pratique pour ses utilisateurs sur de courte distances comme à
    l'intérieur d'un quartier, mais lorsqu'il se doit de traverser une ville entière d'autres
    véhicules tel le métro le feront plus rapidement, sur des distances de plusieurs centaines
    de kilomètres le Bus devient encore plus aberrant.
    <br>
    <br>
    Nous sommes deux étudiants enthousiastes qui avons comme ambition de soutenir des projets
    d’urbanisme ayant pour but de révolutionner la mobilité, c’est ainsi que nous avons créé
    notre Asbl le 2 octobre 2018. Le site a donc un rôle de fondation et de soutien de notre
    association. Notre objectif premier est d’inspirer les villes belges à faire peau neuve
    pour faire face aux défis du siècle à venir.
    </p>
  </div>



